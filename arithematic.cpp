#include<iostream>
using namespace std;

int main(){
    int a,b;
    cout << "Enter two number to perform: ";
    cin >>a>>b;
    cout << "Sum of two: " << a+b << endl;
    cout << "Sub of two: " << a-b << endl;
    cout << "Product of two: " << a*b << endl;
    cout << "Div of two: " << a/b << endl;
    cout << "Mod of two: " << a%b << endl;
    return 0;
}